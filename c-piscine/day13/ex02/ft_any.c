int		ft_any(char **tab, int (*f)(char*))
{
	int i;
	int k;

	i = 0;
	k = 0;
	while (tab[i])
	{
		k = f(tab[i]);
		if (k != 0)
			return (1);
		i++;
	}
	return (0);
}
