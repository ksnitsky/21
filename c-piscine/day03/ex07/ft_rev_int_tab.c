void	ft_rev_int_tab(int *tab, int size)
{
	int a;
	int b;
	int z;

	a = 0;
	b = size - 1;
	while (a < b)
	{
		z = tab[a];
		tab[a] = tab[b];
		tab[b] = z;
		a++;
		b--;
	}
}
